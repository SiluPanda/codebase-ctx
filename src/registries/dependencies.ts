export function categorize(name: string, isDevDep: boolean): DependencyCategory {
  if (DEPENDENCY_REGISTRY[name]) return DEPENDENCY_REGISTRY[name];
  if (name.startsWith('@types/')) return 'type-definitions';
  return isDevDep ? 'build' : 'utility';
}
